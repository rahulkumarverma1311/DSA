package freshStart.bitMapping;

public class BitWiseOperators {

    public static void main(String[] args) {

        // Bit Wish And when both bit is 1 then gives true else false
        System.out.println("bitwise and  "+(3 & 6));
        // Bit Wish or when any of  is 1 then gives true else false
        System.out.println("bitwise or "+(3 | 6));

        // bit wish not means opposite
        int n=-2;
        System.out.println(~n);

    }
}
